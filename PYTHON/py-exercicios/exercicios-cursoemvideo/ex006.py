#Algoritmo que mostra o seu dobro, triplo e raiz quadrada do numero digitado
n = int(input("Digite um numero para descobrir o seu dobro, triplo e raiz quadrada: "))
dobro = n * 2
triplo = n * 3
raiz = n**(1/2)
print(f"O numero digitado é {n}, o dobro dele é {dobro}, o triplo: {triplo} e a raiz é de: {raiz}")