/*

    Os operadores relacionais são usados para comparar valores. O resultado da aplicação desses operadores é um valore boolano, ou seja :true ou false. 
    O programa abaixo mostra o uso desses operadores

 */
public class ex_4_relacionais {
    public static void main (String args[]) {
       int x = 7, y = 8;
       System.out.println("x = " + x);
       System.out.println("y = " + y);
       System.out.println("x < y = " + (x < y));
       System.out.println("x > 10 = " + (x > 10));
       System.out.println("y <= 8 = " + (y <= 8));
       System.out.println("y == 5 = " + (y == 5));
       System.out.println("x != y = " + (x != y));
     }
    }