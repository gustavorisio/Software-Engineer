/*
    O laço for é uma estrutura de repetição muito comum em programação, usada para executar um bloco de código um número determinado de vezes.
*/
public class ex_10_for {
    public static void main(String[] args) {
        int x = 0;
        for (int i = 1; i <= 100; i++)
            x += i;
        System.out.println("A soma de 1 a 100 é: " + x);
    }
}
